# DISSECANDO UMA VÁRIAVEL

nvar = (input('Digite algo... '))

print(f'O tipo do dado inserido é {type(nvar)}')
print(f'A variável "nvar" é apenas espaços: {nvar.isspace()}')
print(f'A variável "nvar" é  númerica: {nvar.isnumeric()}')
print(f'A variável "nvar" é  capitalize: {nvar.isdigit()}')
print(f'A variável "nvar" é decimal: {nvar.isdecimal()}')
print(f'A variável "nvar" é somente maiúsculo: {nvar.isupper()}')
print(f'A variável "nvar" é somente minúsculo: {nvar.islower()}')
print(f'A variável "nvar" é Title: {nvar.istitle()}')
print(f'A variável "nvar" é Diitada: {nvar.isdigit()}')
print(f'A variável "nvar" é ASC II: {nvar.isascii()}')
print(f'A variável "nvar" é Alfabético: {nvar.isalpha()}')
print(f'A variável "nvar" é Alfanumerico: {nvar.isalnum()}')